def get_key_info(number: int, ke: str = "初刻", category: str = "時辰") -> Any:
    if number not in SHAOZI_64_KEYS:
        return f"第{number}數 資料尚未結構化"
    key_data = SHAOZI_64_KEYS[number]
    if category == "特殊":
        return key_data.get("特殊", [])
    if category in key_data:
        sub = key_data[category]
        if isinstance(sub, dict):
            return sub.get(ke, "無資料")
        return sub
    return "查無此類別"

